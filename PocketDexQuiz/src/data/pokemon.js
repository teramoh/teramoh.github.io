// 世代情報
export const getGeneration = (id) => {
  if (id <= 151) return { gen: 1, region: "カントー" };
  if (id <= 251) return { gen: 2, region: "ジョウト" };
  if (id <= 386) return { gen: 3, region: "ホウエン" };
  if (id <= 493) return { gen: 4, region: "シンオウ" };
  if (id <= 649) return { gen: 5, region: "イッシュ" };
  if (id <= 721) return { gen: 6, region: "カロス" };
  if (id <= 809) return { gen: 7, region: "アローラ" };
  if (id <= 905) return { gen: 8, region: "ガラル" };
  if (id <= 1025) return { gen: 9, region: "パルデア" };
  return { gen: 0, region: "不明" };
};

