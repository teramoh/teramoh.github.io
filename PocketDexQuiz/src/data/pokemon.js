// ユニークな分類リストを取得
export const getUniqueCategories = () => {
  return [...new Set(pokemonData.map(p => p.category))];
};

// ユニークなタイプリストを取得
export const getUniqueTypes = () => {
  const types = new Set();
  pokemonData.forEach(p => p.types.forEach(t => types.add(t)));
  return [...types];
};

// ユニークな特性リストを取得
export const getUniqueAbilities = () => {
  const abilities = new Set();
  pokemonData.forEach(p => p.abilities.forEach(a => abilities.add(a)));
  return [...abilities];
};

// ランダムなポケモンを取得
export const getRandomPokemon = () => {
  return pokemonData[Math.floor(Math.random() * pokemonData.length)];
};

// IDでポケモンを取得
export const getPokemonById = (id) => {
  return pokemonData.find(p => p.id === id);
};

// 指定した分類以外のランダムな分類を取得
export const getRandomCategoriesExcept = (correctCategory, count = 3) => {
  const categories = getUniqueCategories().filter(c => c !== correctCategory);
  const shuffled = categories.sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
};

// 指定したタイプ以外のランダムなタイプを取得
export const getRandomTypesExcept = (correctTypes, count = 3) => {
  const types = getUniqueTypes().filter(t => !correctTypes.includes(t));
  const shuffled = types.sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
};

// 指定した特性を持つポケモン以外のランダムなポケモンを取得
export const getRandomPokemonExcept = (exceptId, count = 3) => {
  const filtered = pokemonData.filter(p => p.id !== exceptId);
  const shuffled = filtered.sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
};

// 重さでソートして比較用のポケモンを取得
export const getRandomPokemonForWeightCompare = (correctPokemon) => {
  const filtered = pokemonData.filter(p => 
    p.id !== correctPokemon.id && 
    Math.abs(p.weight - correctPokemon.weight) > 5
  );
  const shuffled = filtered.sort(() => Math.random() - 0.5);
  return shuffled.slice(0, 3);
};

// 高さでソートして比較用のポケモンを取得
export const getRandomPokemonForHeightCompare = (correctPokemon) => {
  const filtered = pokemonData.filter(p => 
    p.id !== correctPokemon.id && 
    Math.abs(p.height - correctPokemon.height) > 0.3
  );
  const shuffled = filtered.sort(() => Math.random() - 0.5);
  return shuffled.slice(0, 3);
};

